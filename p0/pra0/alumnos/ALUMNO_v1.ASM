;_______________________________________________________________
; DEFINICION DEL SEGMENTO DE DATOS
DATOS SEGMENT
	CLR_PANT 	DB 	1BH,"[2","J$"
	CUR_MICRO	DB 	1BH,"4;27fTECNOLOGIA DE COMPUTADORES$"
	PREG_NOM 	DB 	1BH,"[15;1fESCRIBE TU NOMBRE ? $"
	NOMBRE		DB 	80 DUP (0)
	PREG_AP 	DB 	1BH,"[16;1fESCRIBE TU APELLIDO ? $"
	APELLIDO	DB 	80 DUP (0)
	PREG_PAIS 	DB 	1BH,"[17;1fESCRIBE TU PAIS ? $"
	PAIS		DB 	80 DUP (0)
	TEXTO 		DB 	1BH,"[22;1fDON ","$"
	TEXTO1 		DB 	1BH,' '," (FROM ","$"
	TEXTO2 		DB 	") IS COURSING SECOND COURSE OF COMPUTER "
				DB  "SCIENCE",13,10,"$"
	SPACE		DB ' '
DATOS ENDS
;_______________________________________________________________
; DEFINICION DEL SEGMENTO DE PILA
PILA SEGMENT STACK "STACK"
	DB 40H DUP (0)
PILA ENDS
;_______________________________________________________________
; DEFINICION DEL SEGMENTO DE CODIGO
CODE SEGMENT
ASSUME CS:CODE,DS:DATOS,SS:PILA
; COMIENZO DEL PROCEDIMIENTO PRINCIPAL (START)
START PROC FAR
;INICIALIZACION DE LOS REGISTROS DE SEGMENTO
	MOV AX,DATOS
	MOV DS,AX	;FIN DE LAS INICIALIZACIONES
	
	MOV AH,9	; BORRA LA PANTALLA
	MOV DX, OFFSET CLR_PANT
	INT 21H
	
	MOV DX,OFFSET CUR_MICRO		;ESCRIBE "CURSO ...
	INT 21H

	;READ THE NAME
	MOV DX,OFFSET PREG_NOM		;MUESTRA "ESCRIBE ...
	INT 21H                   
	MOV AH,0AH			;ALMACENA EL NOMBRE TECLEADO
	MOV DX,OFFSET NOMBRE
	MOV NOMBRE[0],60		;MAXIMO NUMERO DE CARACTERES
	INT 21H
	
	;READ THE SURNAME
	MOV AH,9
	MOV DX,OFFSET PREG_AP		;MUESTRA "ESCRIBE ...
	INT 21H                   
	MOV AH,0AH			;ALMACENA EL NOMBRE TECLEADO
	MOV DX,OFFSET APELLIDO
	MOV APELLIDO[0],60		;MAXIMO NUMERO DE CARACTERES
	INT 21H
	
	;READ THE COUNTRY
	MOV AH,9
	MOV DX,OFFSET PREG_PAIS		;MUESTRA "ESCRIBE ...
	INT 21H                   
	MOV AH,0AH			;ALMACENA EL NOMBRE TECLEADO
	MOV DX,OFFSET PAIS
	MOV PAIS[0],60		;MAXIMO NUMERO DE CARACTERES
	INT 21H

	MOV AH,9			;ESCRIBE "DON ...
	MOV DX, OFFSET TEXTO
	INT 21H

moV BX,0
WRITE_NAME:
	MOV DL,NOMBRE[BX+2]		;ESCRIBIR UN CARACTER DEL NOMBRE
	MOV AH,2
	INT 21H
	
	INC BX
	CMP BL, NOMBRE[1]		;COMPARA CON EL NUMERO DE CARACTERES
	JNE WRITE_NAME
	
	MOV DL,SPACE   			;PRINT A SPACE
	MOV AH,2
	INT 21H

moV BX,0
WRITE_SURNAME:
	MOV DL,APELLIDO[BX+2]		;ESCRIBIR UN CARACTER DEL APELLIDO
	MOV AH,2
	INT 21H
	
	INC BX
	CMP BL, APELLIDO[1]		;COMPARA CON EL NUMERO DE CARACTERES
	JNE WRITE_SURNAME

	MOV DX, OFFSET TEXTO1		; ESCRIBE " (FROM ...
	MOV AH,9
	INT 21H

moV BX,0
WRITE_COUNTRY:
	MOV DL,PAIS[BX+2]		;ESCRIBIR UN CARACTER DEL PAIS
	MOV AH,2
	INT 21H
	
	INC BX
	CMP BL, PAIS[1]		;COMPARA CON EL NUMERO DE CARACTERES
	JNE WRITE_COUNTRY

	MOV DX, OFFSET TEXTO2		; ESCRIBE " IS COURSING  ...
	MOV AH,9
	INT 21H
	
	MOV AX,4C00H			; FIN DE PROGRAMA Y VUELTA AL DOS
	INT 21H
START ENDP
;FIN DEL SEGMENTO DE CODIGO
CODE ENDS
;FIN DE PROGRAMA INDICANDO DONDE COMIENZA LA EJECUCION
END START

